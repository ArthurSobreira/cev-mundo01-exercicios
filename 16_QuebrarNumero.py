from math import trunc

num = float(input('Digite um número:'))
print(f'A parte inteira do número {num} é igual a {trunc(num)}')

num = float(input('Digite um número:'))
print(f'A parte inteira de {num} é {num:.0f}')

num = float(input('Digite um número:'))
print(f'A parte inteira de {num} é {int(num)}')
